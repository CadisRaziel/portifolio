abstract class AppImages {
  String get vitor;
  String get universe;
  String get contato;
}

class AppImagesTheme extends AppImages {
  @override
  String get vitor => "assets/images/vitor.png";
  @override
  String get universe => "assets/images/universe.jpg";
  @override
  String get contato => "assets/images/contato.png";
}
